'use client'

import React, { useState } from 'react'
import { useRouter, useSearchParams } from 'next/navigation'
import { supabase } from '@/lib/supabase/client'
import { Button } from '@/components/ui/button'
import { Input } from '@/components/ui/input'
import { Alert, AlertDescription } from '@/components/ui/alert'

// Known admin emails - check profile for admin status after login
const ADMIN_EMAILS = [
  'benjamin@act.place',
  'knighttss@gmail.com',
  'ben@empathyledger.com',
]

interface SimpleSignInFormProps {
  redirectTo?: string
}

export function SimpleSignInForm({ redirectTo: propRedirectTo }: SimpleSignInFormProps) {
  const searchParams = useSearchParams()
  const urlRedirect = searchParams?.get('redirect')
  const defaultRedirect = propRedirectTo || urlRedirect || '/'

  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const [isLoading, setIsLoading] = useState(false)
  const [error, setError] = useState('')
  const [success, setSuccess] = useState(false)
  const [useMagicLink, setUseMagicLink] = useState(true) // Default to magic link
  const [magicLinkSent, setMagicLinkSent] = useState(false)
  const router = useRouter()

  const handleSignIn = async (e: React.FormEvent) => {
    e.preventDefault()
    console.log('üîê Starting signin process...')
    setIsLoading(true)
    setError('')
    setSuccess(false)

    try {
      if (!email || !password) {
        setError('Please enter both email and password')
        setIsLoading(false)
        return
      }

      console.log('üîê Attempting authentication for:', email)
      const { data, error } = await supabase.auth.signInWithPassword({
        email: email.trim(),
        password,
      })

      if (error) {
        console.error('üîê Authentication failed:', error.message)
        setError(error.message)
        setIsLoading(false)
        return
      }

      if (data.user && data.session) {
        console.log('üîê Authentication successful!')
        setSuccess(true)

        // Check if user is admin and determine redirect
        const userEmail = data.user.email?.toLowerCase() || ''
        const isLikelyAdmin = ADMIN_EMAILS.some(e => e.toLowerCase() === userEmail)

        // Also check profile for admin status
        const { data: profile } = await supabase
          .from('profiles')
          .select('tenant_roles, super_admin')
          .eq('id', data.user.id)
          .single()

        const hasAdminRole = profile?.tenant_roles?.includes('admin') ||
                           profile?.tenant_roles?.includes('super_admin') ||
                           profile?.super_admin === true

        // Determine final redirect
        let finalRedirect = defaultRedirect
        if ((isLikelyAdmin || hasAdminRole) && !urlRedirect) {
          // Admin without specific redirect ‚Üí send to admin panel
          finalRedirect = '/admin'
        }

        console.log('üîê Redirecting to:', finalRedirect)

        // Shorter delay for better UX
        setTimeout(() => {
          window.location.replace(finalRedirect)
        }, 1000)
      } else {
        setError('Authentication failed - no user data received')
        setIsLoading(false)
      }
    } catch (error) {
      console.error('üîê Signin exception:', error)
      setError('An unexpected error occurred. Please try again.')
      setIsLoading(false)
    }
  }

  const handleMagicLinkSignIn = async (e: React.FormEvent) => {
    e.preventDefault()
    console.log('‚ú® Starting magic link signin...')
    setIsLoading(true)
    setError('')
    setMagicLinkSent(false)

    try {
      if (!email) {
        setError('Please enter your email address')
        setIsLoading(false)
        return
      }

      console.log('‚ú® Sending magic link to:', email)
      const { error } = await supabase.auth.signInWithOtp({
        email: email.trim(),
        options: {
          emailRedirectTo: `${window.location.origin}/auth/callback?redirect=${encodeURIComponent(defaultRedirect)}`,
        }
      })

      if (error) {
        console.error('‚ú® Magic link failed:', error.message)
        setError(error.message)
        setIsLoading(false)
        return
      }

      console.log('‚ú® Magic link sent successfully!')
      setMagicLinkSent(true)
      setIsLoading(false)
    } catch (error) {
      console.error('‚ú® Magic link exception:', error)
      setError('Failed to send magic link. Please try again.')
      setIsLoading(false)
    }
  }

  const handleOAuthSignIn = async (provider: 'google' | 'github' | 'azure') => {
    console.log(`üîê Starting ${provider} OAuth signin...`)
    setIsLoading(true)
    setError('')

    try {
      const { data, error } = await supabase.auth.signInWithOAuth({
        provider,
        options: {
          redirectTo: `${window.location.origin}/auth/callback?redirect=${encodeURIComponent(defaultRedirect)}`,
          queryParams: provider === 'google' ? {
            prompt: 'select_account'
          } : undefined
        }
      })

      if (error) {
        console.error(`üîê ${provider} OAuth failed:`, error.message)
        setError(error.message)
        setIsLoading(false)
        return
      }

      console.log(`üîê ${provider} OAuth initiated successfully`)
      // OAuth will redirect, so we don't need to handle success here
    } catch (error) {
      console.error(`üîê ${provider} signin exception:`, error)
      setError(`Failed to initiate ${provider} sign-in. Please try again.`)
      setIsLoading(false)
    }
  }

  return (
    <div className="space-y-6">
      {/* Magic Link Success Message */}
      {magicLinkSent && (
        <Alert variant="default" className="border-green-200 bg-green-50 text-green-800">
          <AlertDescription className="flex items-center gap-2">
            <span className="text-2xl">‚ú®</span>
            <div>
              <strong>Check your email!</strong>
              <p className="text-sm mt-1">
                We sent a magic link to <strong>{email}</strong>. Click the link in the email to sign in instantly - no password needed!
              </p>
            </div>
          </AlertDescription>
        </Alert>
      )}

      {/* Magic Link Form (Default) */}
      {useMagicLink ? (
        <form onSubmit={handleMagicLinkSignIn} className="space-y-4" aria-label="Sign in with magic link">
          <div className="space-y-2">
            <label htmlFor="magic-email" className="text-base font-medium">
              Email Address
            </label>
            <Input
              id="magic-email"
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              placeholder="your.email@example.com"
              required
              disabled={isLoading || magicLinkSent}
              autoFocus
              autoComplete="email"
              aria-describedby="magic-link-help"
              className="text-base min-h-[48px]"
            />
            <p id="magic-link-help" className="text-sm text-stone-600">
              Enter your email to receive a secure sign-in link. No password needed!
            </p>
          </div>

          {error && !magicLinkSent && (
            <Alert variant="destructive" role="alert">
              <AlertDescription>{error}</AlertDescription>
            </Alert>
          )}

          <Button
            type="submit"
            className="w-full bg-sage-600 hover:bg-sage-700 min-h-[48px] text-base"
            disabled={isLoading || magicLinkSent}
            aria-label="Send magic link to your email"
          >
            {magicLinkSent ? '‚úÖ Magic Link Sent!' : isLoading ? '‚è≥ Sending...' : '‚ú® Send Magic Link'}
          </Button>

          <div className="text-center text-sm text-stone-600 space-y-2">
            <p>We'll email you a secure sign-in link. No password to remember!</p>
            {magicLinkSent && (
              <p className="text-xs text-stone-500">
                Haven't received it? Check your spam folder or{' '}
                <button
                  type="button"
                  onClick={() => {
                    setMagicLinkSent(false)
                    setError('')
                  }}
                  className="text-sage-600 hover:text-sage-700 underline"
                >
                  try again
                </button>
              </p>
            )}
          </div>
        </form>
      ) : (
        /* Password Form (Alternative) */
        <form onSubmit={handleSignIn} className="space-y-4">
        <div className="space-y-2">
          <label htmlFor="email" className="text-sm font-medium">
            Email Address
          </label>
          <Input
            id="email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            placeholder="your.email@example.com"
            required
            disabled={isLoading}
          />
        </div>

        <div className="space-y-2">
          <label htmlFor="password" className="text-sm font-medium">
            Password
          </label>
          <Input
            id="password"
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            placeholder="Enter your password"
            required
            disabled={isLoading}
          />
        </div>

        {error && (
          <Alert variant="destructive">
            <AlertDescription>{error}</AlertDescription>
          </Alert>
        )}

        {success && (
          <Alert variant="default" className="border-green-200 bg-green-50 text-green-800">
            <AlertDescription>‚úÖ Login successful! Redirecting...</AlertDescription>
          </Alert>
        )}

        <Button
          type="submit"
          className="w-full"
          disabled={isLoading || success}
        >
          {success ? '‚úÖ Success! Redirecting...' : isLoading ? '‚è≥ Signing In...' : 'Sign In with Password'}
        </Button>
      </form>
      )}

      {/* Toggle between Magic Link and Password */}
      <div className="text-center">
        <button
          type="button"
          onClick={() => setUseMagicLink(!useMagicLink)}
          className="text-sm text-sage-600 hover:text-sage-700 underline"
        >
          {useMagicLink ? 'Use password instead' : 'Use magic link instead (easier!)'}
        </button>
      </div>

      {/* Divider */}
      <div className="relative">
        <div className="absolute inset-0 flex items-center">
          <div className="w-full border-t border-stone-300" />
        </div>
        <div className="relative flex justify-center text-sm">
          <span className="px-2 bg-white text-stone-500">Or continue with</span>
        </div>
      </div>

      {/* Social OAuth Section */}
      <div className="space-y-3">
        {/* GitHub OAuth */}
        <Button
          type="button"
          onClick={() => handleOAuthSignIn('github')}
          className="w-full bg-[#24292F] hover:bg-[#1B1F23] text-white flex items-center justify-center gap-3"
          disabled={isLoading || success || magicLinkSent}
          aria-label="Sign in with GitHub"
        >
          <svg className="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
            <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"/>
          </svg>
          Continue with GitHub
        </Button>

        {/* Microsoft OAuth */}
        <Button
          type="button"
          onClick={() => handleOAuthSignIn('azure')}
          className="w-full bg-white border border-stone-300 text-stone-700 hover:bg-stone-50 flex items-center justify-center gap-3"
          disabled={isLoading || success || magicLinkSent}
          aria-label="Sign in with Microsoft"
        >
          <svg className="w-5 h-5" viewBox="0 0 23 23" fill="none">
            <path d="M0 0h11v11H0z" fill="#f25022"/>
            <path d="M12 0h11v11H12z" fill="#00a4ef"/>
            <path d="M0 12h11v11H0z" fill="#ffb900"/>
            <path d="M12 12h11v11H12z" fill="#7fba00"/>
          </svg>
          Continue with Microsoft
        </Button>

        {/* Google OAuth */}
        <Button
          type="button"
          onClick={() => handleOAuthSignIn('google')}
          className="w-full bg-white border border-stone-300 text-stone-700 hover:bg-stone-50 flex items-center justify-center gap-3"
          disabled={isLoading || success || magicLinkSent}
          aria-label="Sign in with Google"
        >
          <svg className="w-5 h-5" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          Continue with Google
        </Button>

        {/* Data sovereignty notice */}
        <p className="text-xs text-center text-stone-500 mt-4">
          Social sign-in shares your email with the provider.{' '}
          <button
            type="button"
            onClick={() => setUseMagicLink(true)}
            className="text-sage-600 hover:text-sage-700 underline"
          >
            Use magic link
          </button>
          {' '}for more privacy.
        </p>
      </div>
    </div>
  )
}